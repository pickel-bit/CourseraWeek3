## The two following functions are to be used together in order to cache
## the value of a matrix's inverse and to use this to skip unnecessary
## computational steps when computing matrix inverses.
## Example commands: 
## z<-makeCacheMatrix(cbind(c(1,2,3),c(3,4,7),c(7,8,5)))
## cacheSolve(z)
## -> if the command "cacheSolve(z)" is repeated, there will be a message
## "getting cached data" since the functions now skip he actual matrx 
## inverse computation

## The first function makeCacheMatrix creates a special matrix which is
## really a list containing functions to
##1.set the value of the matrix
##2.get the value of the matrix
##3.set the value of the inverse
##4.get the value of the inverse

makeCacheMatrix <- function(x = matrix()) {
    inv <- NULL
    set <- function(y) {
      x <<- y
      inv <<- NULL
    }
    get <- function() x
    setinv <- function(inverse) inv <<- inverse
    getinv <- function() inv
    list(set = set, get = get,
         setinv = setinv,
         getinv = getinv)
  }

## The following function calculates the inverse of the special matrix created
## with the above function. However, it first checks to see if the inverse has
## already been calculated. If so, it gets it from the cache and skips the
## computation. Otherwise, it calculates the inverse of the matrix and sets the
## value of the inverse in the cache via the setinv function.

cacheSolve <- function(x, ...) {
    inv <- x$getinv()
    if(!is.null(inv)) {
      message("getting cached data")
      return(inv)
    }
    data <- x$get()
    inv <- solve(data, ...)
    x$setinv(inv)
    inv
  }